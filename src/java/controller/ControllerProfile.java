    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        HttpSession session = request.getSession();

        //Lấy thông tin người dùng (bằng name của account) từ cơ sở dữ liệu
        UserLoginDAO userlgDao = new UserLoginDAO();
        //Lấy account (bao gồm accountName & password người dùng input) từ session
        UserLogin accountLogin = (UserLogin) session.getAttribute("accountSession"); //This account just contains 2 field: accountName and password
        //Từ đó sử dụng accountName trên để truy xuất trong cơ sở dữ liệu => lấy được thông tin của người dùng
        UserLogin getUserByAccount = userlgDao.getUserLoginByAccountName(accountLogin.getAccountName());
        
        //Đưa dữ liệu tới trang Profile để hiển thị
        request.setAttribute("user", getUserByAccount);
        request.getRequestDispatcher("Profile.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

